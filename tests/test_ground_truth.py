# Test 4: Orbit expansion
# =============================================================================

@pytest.mark.slow
@pytest.mark.parametrize("h11_idx", _orbit_params())
def test_orbit_expansion(h11_idx):
    """Validate cybir orbit expansion against cached ground truth.

    At generic CS (reflections='hekc'), the orbit should include all phases
    the original code found. At tuned CS (reflections='ekc'), cybir may
    find fewer phases since SU2_NONGENERIC_CS walls are not traversed.
    """
    h11, poly_idx = h11_idx
    gt_dir = GROUND_TRUTH_DIR / f"h11_{h11}"
    with open(gt_dir / f"poly_{poly_idx}.json") as f:
        gt_json = json.load(f)
    gt_npz = np.load(gt_dir / f"poly_{poly_idx}.npz")
    full_bfs = gt_json["full_bfs"]

    # Run cybir with HEKC orbit expansion (generic CS)
    cy = _get_cy(h11, poly_idx)
    gvs = _load_gvs(h11, poly_idx)
    ekc = CYBirationalClass.from_gv(cy, max_deg=10, verbose=False, gvs=gvs)
    ekc.apply_coxeter_orbit(reflections='hekc', phases=True)

    # Every ground truth full BFS phase should exist in HEKC
    n_gt = 0
    while f"full_phase_{n_gt}_int_nums" in gt_npz:
        n_gt += 1

    for gt_idx in range(n_gt):
        gt_int = gt_npz[f"full_phase_{gt_idx}_int_nums"]
        gt_c2 = gt_npz[f"full_phase_{gt_idx}_c2"]
        found = any(
            np.array_equal(p.int_nums, gt_int)
            and np.array_equal(p.c2, gt_c2)
            for p in ekc.phases
        )
        assert found, (
            f"Ground truth full BFS phase {gt_idx} not found in cybir HEKC "
            f"({len(ekc.phases)} phases vs ground truth {n_gt})"
        )

    gt_npz.close()
