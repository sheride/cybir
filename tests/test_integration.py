# Classification tests
# ---------------------------------------------------------------------------


class TestClassification:
    """Verify classify_contraction against snapshots."""

    @pytest.fixture(autouse=True)
    def _check_fixtures(self):
        _skip_if_no_fixtures()

    @pytest.mark.parametrize(
        "wall_pair", _ALL_WALLS, ids=[_wall_id_str(p) for p in _ALL_WALLS]
    )
    def test_classification_matches_snapshot(self, wall_pair):
        data, idx = wall_pair
        wall = data["walls"][idx]

        original_category = wall["contraction_type"]

        # Skip potent walls (the original code doesn't classify these
        # as one of the 5 standard types)
        if "potent" in original_category.lower() or "insufficient" in original_category.lower():
            pytest.skip(f"Potent/insufficient wall: {original_category}")

        expected_type = CATEGORY_MAP.get(original_category)
        if expected_type is None:
            pytest.skip(f"Unknown original category: {original_category}")

        int_nums = np.array(wall["int_nums"])
        c2 = np.array(wall["c2"])
        curve = np.array(wall["curve"])

        # For asymptotic/CFT walls, gv_series may be None -- classify_contraction
        # handles these before touching gv_series.
        gv_series = wall["gv_series"] if wall["gv_series"] is not None else []

        # Asymptotic and CFT don't need gv_series
        if expected_type in (ContractionType.ASYMPTOTIC, ContractionType.CFT):
            # Use a dummy gv_series since classify checks asymptotic/CFT first
            gv_series = [1, 0]

        result = classify_contraction(int_nums, c2, curve, gv_series)
        result_type = result["contraction_type"]

        assert result_type == expected_type, (
            f"Classification mismatch for polytope {data['polytope_id']} "
            f"wall {idx}: got {result_type.name}, expected {expected_type.name} "
            f"(original: {original_category})"
        )
